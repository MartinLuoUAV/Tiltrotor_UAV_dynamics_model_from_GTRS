% =========================================================================
% 局部函数定义区 (必须放在脚本文件的最末尾)
% =========================================================================

function vq = clamp_interp(x, v, xq)
    % 执行内部定义区线性插值
    vq = interp1(x, v, xq, 'linear'); 
    
    % 外部边界平推锁定 (Clamping)
    % xq < 最小风洞测试迎角 时，等于边界值
    vq(xq < x(1))   = v(1);           
    
    % xq > 最大风洞测试迎角 时，等于边界值
    vq(xq > x(end)) = v(end);         
end