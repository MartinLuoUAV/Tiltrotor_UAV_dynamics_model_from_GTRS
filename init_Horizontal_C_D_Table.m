% =========================================================================
% 文件名: init_Tail_CDH_2D_Table.m
% 描述: XV-15 平尾阻力系数 C_DH 二维气动数据库 (去冗余，带边界平推保护)
% 维度: [Alpha_HD (49) x Mach (4)]
% 数据来源: GTRS 报告 Table 5-III
% =========================================================================

% -------------------------------------------------------------------------
% 1. 定义二维断点向量 (Breakpoints)
% -------------------------------------------------------------------------
% 维度 1: 平尾阻力迎角 Alpha_HD (提取 Table 5-III 的 49 个全包线断点)
alpha_HD_deg = [-180, -170, -160, -150, -140, -130, -120, -110, -100, -90, -80, -70, -60, -50, -40, -36, -32, -28, -24, -20, -16, -12, -8, -4, 0, 4, 8, 12, 16, 20, 24, 28, 32, 36, 40, 50, 60, 70, 80, 90, 100, 110, 120, 130, 140, 150, 160, 170, 180];
alpha_HD_rad = alpha_HD_deg * (pi/180);

% 维度 2: 马赫数 Mach_N (4个基准点)
mach_vec = [0.2, 0.4, 0.5, 0.6];

% -------------------------------------------------------------------------
% 2. 载入核心低速基准数据 (Mach = 0-0.2)
% -------------------------------------------------------------------------
CDH_M02_Base = [0.01, 0.02, 0.20, 0.40, 0.55, 0.67, 0.78, 0.85, 0.89, 0.92, 0.91, 0.87, 0.81, 0.72, 0.60, 0.54, 0.47, 0.39, 0.30, 0.20, 0.115, 0.068, 0.035, 0.015, 0.00875, 0.015, 0.035, 0.068, 0.115, 0.20, 0.34, 0.48, 0.61, 0.72, 0.80, 0.93, 1.05, 1.14, 1.18, 1.20, 1.19, 1.14, 1.06, 0.96, 0.80, 0.60, 0.36, 0.02, 0.01];

% -------------------------------------------------------------------------
% 3. 提取高马赫数局部数据并计算增量 (Delta = Mach - Base)
% -------------------------------------------------------------------------
% Mach = 0.4 (定义域: -16 到 16)
alpha_M04 = [-16, -12, -8, -4, 0, 4, 8, 12, 16];
CDH_M04_raw = [0.135, 0.068, 0.035, 0.015, 0.00875, 0.015, 0.035, 0.075, 0.145];
CDH_M02_for_M04 = [0.115, 0.068, 0.035, 0.015, 0.00875, 0.015, 0.035, 0.068, 0.115];
Delta_M04_raw = CDH_M04_raw - CDH_M02_for_M04;

% Mach = 0.5 (定义域: -12 到 12)
alpha_M05 = [-12, -8, -4, 0, 4, 8, 12];
CDH_M05_raw = [0.088, 0.035, 0.015, 0.00875, 0.015, 0.045, 0.105];
CDH_M02_for_M05 = [0.068, 0.035, 0.015, 0.00875, 0.015, 0.035, 0.068];
Delta_M05_raw = CDH_M05_raw - CDH_M02_for_M05;

% Mach = 0.6 (定义域: -8 到 8)
alpha_M06 = [-8, -4, 0, 4, 8];
CDH_M06_raw = [0.045, 0.015, 0.00875, 0.015, 0.065];
CDH_M02_for_M06 = [0.035, 0.015, 0.00875, 0.015, 0.035];
Delta_M06_raw = CDH_M06_raw - CDH_M02_for_M06;

% -------------------------------------------------------------------------
% 4. 使用 clamp_interp 平推增量至 49 个断点全范围
% -------------------------------------------------------------------------
Delta_M04_full = clamp_interp(alpha_M04, Delta_M04_raw, alpha_HD_deg);
Delta_M05_full = clamp_interp(alpha_M05, Delta_M05_raw, alpha_HD_deg);
Delta_M06_full = clamp_interp(alpha_M06, Delta_M06_raw, alpha_HD_deg);

% -------------------------------------------------------------------------
% 5. 初始化 2D 矩阵并填充 (尺寸: 49行 x 4列)
% -------------------------------------------------------------------------
CDH_2D_table = zeros(length(alpha_HD_deg), length(mach_vec));

% M = 0.2 (基础列)
CDH_2D_table(:, 1) = CDH_M02_Base;
% M = 0.4
CDH_2D_table(:, 2) = CDH_M02_Base + Delta_M04_full;
% M = 0.5
CDH_2D_table(:, 3) = CDH_M02_Base + Delta_M05_full;
% M = 0.6
CDH_2D_table(:, 4) = CDH_M02_Base + Delta_M06_full;

% =========================================================================
% 局部函数定义区 (严格放在脚本最末尾)
% =========================================================================

function vq = clamp_interp(x, v, xq)
    % 执行内部定义区线性插值
    vq = interp1(x, v, xq, 'linear'); 
    
    % 外部边界平推锁定 (Clamping)
    vq(xq < x(1))   = v(1);           
    vq(xq > x(end)) = v(end);         
end