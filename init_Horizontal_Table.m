% =========================================================================
% 局部函数定义区 (严格放在脚本最末尾)
% =========================================================================

function vq = clamp_interp(x, v, xq)
    % 执行内部定义区线性插值
    vq = interp1(x, v, xq, 'linear'); 
    
    % 外部边界平推锁定 (Clamping)
    % 当查询点 < 最小风洞测试迎角 (-40) 时，锁定为 -40 度时的值
    vq(xq < x(1))   = v(1);           
    
    % 当查询点 > 最大风洞测试迎角 (+40) 时，锁定为 +40 度时的值
    vq(xq > x(end)) = v(end);         
end